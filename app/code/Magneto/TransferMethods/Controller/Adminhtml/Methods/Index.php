<?php

namespace Magneto\TransferMethods\Controller\Adminhtml\Methods;

use Magento\Backend\App\Action\Context;
use Magento\Framework\View\Result\PageFactory;

class Index extends \Magento\Backend\App\Action
{
    /**
     * @var \Magento\Framework\View\Result\PageFactory
     */
    protected $resultPageFactory;

    /**
     * @param \Magento\Backend\App\Action\Context $context
     * @param \Magento\Framework\View\Result\PageFactory resultPageFactory
     */
    public function __construct(
        Context $context,
        \Magento\Framework\View\Result\PageFactory $resultPageFactory
    )
    {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }
    /**
     * Default customer account page
     *
     * @return void
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Magneto_TransferMethods::method');
        $resultPage->addBreadcrumb(__('Transfer Methods'), __('Methods'));
        $resultPage->addBreadcrumb(__('Transfer Methods'), __('Methods'));
        $resultPage->getConfig()->getTitle()->prepend(__('Transfer Methods'));

        return $resultPage;
    }
}
?>